    <p><b>Sécurité et protection des données :</b> les données sont stockées uniquement dans votre navigateur. Aucune donnée n'est collectée. NumÉcoDiag fonctionne seule sans communication réseau. Un export CSV est disponible pour exploiter les résultats du diagnostic. Le code est audité automatiquement par l'outil d'audit de sécurité <a href="https://marketplace.visualstudio.com/items?itemName=snyk-security.snyk-vulnerability-scanner" target="_blank">Snyk</a>.</p>
