    <h2>À propos</h2>
    <div>
        <img src="./static/logo-minumeco.svg" alt="Logo MiNumEco" />
        <p>Cet outil développé et mis à disposition gratuitement par la <a href="https://ecoresponsable.numerique.gouv.fr/" target="_blank">MiNumEco</a> facilite l'auto-diagnostic de l’écoconception d’un service numérique, conformément au <a href="https://ecoresponsable.numerique.gouv.fr/publications/referentiel-general-ecoconception/" target="_blank">Référentiel Général d'Écoconception de Services Numériques</a> (RGESN).</p>
    </div>
